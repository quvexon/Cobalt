  fun colorLerp(from: Color, to: Color, progress: Float): Color {
    val p = progress.coerceIn(0f, 1f)
    val r = (from.red + (to.red - from.red) * p).toInt()
    val g = (from.green + (to.green - from.green) * p).toInt()
    val b = (from.blue + (to.blue - from.blue) * p).toInt()
    val a = (from.alpha + (to.alpha - from.alpha) * p).toInt()
    return Color(r, g, b, a)
  }

  fun rainbow(hue: Float): Color = Color.getHSBColor(hue, 1f, 1f)
